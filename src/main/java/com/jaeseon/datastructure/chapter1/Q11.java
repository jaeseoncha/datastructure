package com.jaeseon.datastructure.chapter1;

public class Q11 {

    public static void main(String[] args) {

        gugudan();
    }

    public static void gugudan(){

        for ( int i =1; i <=9 ; i++){

            for ( int j =2; j <=9 ; j++ ){

                System.out.println( i + " * " + j + " = " + i*j);

            }
        }

    }

}
